def recommend_by_fridge_and_season(user_ingredients_list, current_month=None, top_n=5):

    if current_month is None:
        current_month = datetime.now().month

    if current_month in [12, 1, 2]:
        season = 'Winter'
    elif current_month in [3, 4, 5]:
        season = 'Spring'
    elif current_month in [6, 7, 8]:
        season = 'Summer'
    else:
        season = 'autumn'

    seasonal = df[df['season'] == season].copy()

    def count_matching(ingredients_str):
        if not isinstance(ingredients_str, str):
            return 0
        ing_set = set(ingredients_str.lower().split())
        user_set = set([i.lower() for i in user_ingredients_list])
        return len(ing_set & user_set)

    seasonal['match_count'] = seasonal['ingredients'].apply(count_matching)

    matches = seasonal[seasonal['match_count'] > 0].copy()
    matches = matches.sort_values(['match_count', 'minutes'], ascending=[False, True])

    return matches[['name', 'match_count', 'minutes', 'n_steps', 'main_ingredient']].head(top_n)

