def recommend_seasonal_meals(current_month=None, top_n=10):
    if current_month is None:
        current_month = datetime.now().month

    if current_month in [12, 1, 2]:
        season = 'Winter'
    elif current_month in [3, 4, 5]:
        season = 'Spring'
    elif current_month in [6, 7, 8]:
        season = 'Summer'
    else:
        season = 'autumn'

    candidates = df[
        (df['season'] == season) &
        (df['n_steps'] >= 3) &
        (df['n_ingredients'] >= 3) &
        (df['minutes'] >= 5) &
        (df['minutes'] <= 180)
    ].copy()

    exclude_keywords = ['sauce', 'dressing', 'drink', 'cocktail', 'shot', 'marinade', 'dip', 'spice mix', 'seasoning']
    mask = ~candidates['name'].str.lower().str.contains('|'.join(exclude_keywords), na=False)
    if 'tags' in candidates.columns:
        mask &= ~candidates['tags'].str.lower().str.contains('|'.join(exclude_keywords), na=False)
    candidates = candidates[mask]

    candidates = candidates.sort_values(['n_steps', 'minutes'], ascending=[True, True])

    return candidates[['name', 'minutes', 'n_steps', 'main_ingredient_v2', 'description']].head(top_n)


